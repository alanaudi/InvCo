class Color:
    """ ANSI Color Code """

    WHITE     = '\033[1;37m'
    BLACK     = '\033[0;30m'
    RED       = '\033[0;31m'
    YELLOW    = '\033[1;33m'
    GRAY      = '\033[0;37m'

    OKBLUE    = '\033[94m'
    OKGREEN   = '\033[92m'
    WARNING   = '\033[93m'
    FAIL      = '\033[91m'

    BOLD      = '\033[1m'
    HEADER    = '\033[95m'
    UNDERLINE = '\033[4m'
    ENDC      = '\033[0m'
